package com.ontytoom.webmail.seleniumTest.exceptions;

public class NotImplementedException extends TestException
{

	public static final long serialVersionUID = 0L;

	public NotImplementedException()
	{
		super();
	}

	public NotImplementedException(String message)
	{
		super(message);
	}

	public NotImplementedException(Throwable cause)
	{
		super(cause);
	}

	public NotImplementedException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public NotImplementedException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
