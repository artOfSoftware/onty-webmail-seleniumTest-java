package com.ontytoom.webmail.seleniumTest.exceptions;

public class IndeterminateException extends TestException
{

	public static final long serialVersionUID = 0L;

	public IndeterminateException()
	{
		super();
	}

	public IndeterminateException(String message)
	{
		super(message);
	}

	public IndeterminateException(Throwable cause)
	{
		super(cause);
	}

	public IndeterminateException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public IndeterminateException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
